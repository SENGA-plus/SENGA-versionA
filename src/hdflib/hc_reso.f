
C     HDF Collective Restart Output
C     -----------------------------
      
      SUBROUTINE HC_RESO(FNHDFF)
      
      USE HDF5
      USE MPI
      INCLUDE '../com_senga2.h'

      INTEGER, PARAMETER :: NDIM = 3, NSDIM = 1
      INTEGER(HSIZE_T) GDIM(NDIM), LDIM(NDIM), ADIM(NSDIM)
      INTEGER(HSIZE_T) OFFSET(NDIM)
      
      INTEGER, PARAMETER :: NXDIM = 1, NYDIM = 1, NZDIM = 1
      INTEGER(HSIZE_T) GXDIM(NXDIM), GYDIM(NYDIM), GZDIM(NZDIM)
      INTEGER(HSIZE_T) LXDIM(NXDIM), LYDIM(NYDIM), LZDIM(NZDIM)                  
      INTEGER(HSIZE_T) OFFSETX(NXDIM), OFFSETY(NYDIM), OFFSETZ(NZDIM)
      INTEGER(HSIZE_T) STRIDEX(NXDIM), STRIDEY(NYDIM), STRIDEZ(NZDIM)
      INTEGER(HSIZE_T) COUNTX(NXDIM), COUNTY(NYDIM), COUNTZ(NZDIM)
      INTEGER(HSIZE_T) BLOCKX(NXDIM), BLOCKY(NYDIM), BLOCKZ(NZDIM)

      INTEGER(HSIZE_T) COUNT(NDIM), STRIDE(NDIM), BLOCK(NDIM)
      
      DOUBLE PRECISION XARRY(NXNODE),YARRY(NYNODE),ZARRY(NZNODE)
      DOUBLE PRECISION ATIME(3), AERROR(2),AINLET(3)

      INTEGER IC, JC, KC, ISPEC
      INTEGER ERROR,IGOFST,JGOFST, KGOFST, IX,JY,KZ,ASIZE(4)
      INTEGER(HID_T) GID,FID,DID,PID,MID,SID, AID

      CHARACTER*60 FNHDFF
      CHARACTER*6  PNPROC
      CHARACTER*3  SPECVN

      LOGICAL BCFLAG
      
      GDIM(1) = NXGLBL
      GDIM(2) = NYGLBL
      GDIM(3) = NZGLBL
      
      LDIM(1) = NXNODE
      LDIM(2) = NYNODE
      LDIM(3) = NZNODE

      ADIM(1) = 1

      LXDIM(1) = NXNODE
      LYDIM(1) = NYNODE
      LZDIM(1) = NZNODE
      
      GXDIM(1) = NXGLBL
      GYDIM(1) = NYGLBL
      GZDIM(1) = NZGLBL
      
      IGOFST = 0
      DO IC = 0, IXPROC-1
         IGOFST = IGOFST + NPMAPX(IC)
      ENDDO

      DO IC = ISTAL,ISTOL
         IX = IGOFST + IC
         XARRY(IC) = REAL(IX-1)*DELTAX
      ENDDO

      JGOFST = 0
      DO JC = 0, IYPROC-1
         JGOFST = JGOFST + NPMAPY(JC)
      ENDDO

      DO JC = JSTAL,JSTOL
         JY = JGOFST + JC
         YARRY(JC) = REAL(JY-1)*DELTAY
      ENDDO

      KGOFST = 0
      DO KC = 0, IZPROC-1
         KGOFST = KGOFST + NPMAPZ(KC)
      ENDDO

      DO KC = KSTAL,KSTOL
         KZ = KGOFST + KC
         ZARRY(KC) = REAL(KZ-1)*DELTAZ
      ENDDO      
            
C     INITIALIZE FORTRAN INTERFACE
      CALL H5OPEN_F (ERROR)

C     CREATE PROCESS LIST AND FILE IN COLLECTIVE MODE
      CALL H5PCREATE_F(H5P_FILE_ACCESS_F, PID, ERROR)
      CALL H5PSET_FAPL_MPIO_F(PID, MPI_COMM_WORLD, MPI_INFO_NULL, ERROR)
      
      CALL H5FCREATE_F(FNHDFF, H5F_ACC_TRUNC_F, FID, ERROR,
     $     ACCESS_PRP = PID)
      CALL H5PCLOSE_F(PID, ERROR)

      CALL H5GCREATE_F(FID,'/Grid',GID,ERROR)

C     IF(IPROC.EQ.0)THEN
      ADIM(1) = 3
      ATIME(1) = ETIME
      ATIME(2) = TSTEP
      ATIME(3) = ITIME
      
      CALL H5SCREATE_SIMPLE_F(NSDIM, ADIM, SID, ERROR)
      CALL H5ACREATE_F(GID, 'Time', H5T_NATIVE_DOUBLE,
     $     SID, AID,ERROR)
      CALL H5AWRITE_F(AID, H5T_NATIVE_DOUBLE, ATIME, ADIM, ERROR)
      CALL H5ACLOSE_F(AID, ERROR)      
      CALL H5SCLOSE_F(SID, ERROR)

      ADIM(1) = 4
      ASIZE(1) = NXGLBL
      ASIZE(2) = NYGLBL
      ASIZE(3) = NZGLBL
      ASIZE(4) = NSPEC
      
      CALL H5SCREATE_SIMPLE_F(NSDIM, ADIM, SID, ERROR)
      CALL H5ACREATE_F(GID, 'Size', H5T_NATIVE_INTEGER,
     $     SID, AID,ERROR)
      CALL H5AWRITE_F(AID, H5T_NATIVE_INTEGER, ASIZE, ADIM, ERROR)
      CALL H5ACLOSE_F(AID, ERROR)      
      CALL H5SCLOSE_F(SID, ERROR)

      ADIM(1) = 2
      AERROR(1) = ERROLD
      AERROR(2) = ERRLDR
      
      CALL H5SCREATE_SIMPLE_F(NSDIM, ADIM, SID, ERROR)
      CALL H5ACREATE_F(GID, 'Error', H5T_NATIVE_DOUBLE,
     $     SID, AID,ERROR)
      CALL H5AWRITE_F(AID, H5T_NATIVE_DOUBLE, AERROR, ADIM, ERROR)
      CALL H5ACLOSE_F(AID, ERROR)      
      CALL H5SCLOSE_F(SID, ERROR)      

C     ENDIF
      
      OFFSETX(1) = IGOFST
      OFFSETY(1) = JGOFST
      OFFSETZ(1) = KGOFST            
      
      COUNTX(1) = 1
      COUNTY(1) = 1
      COUNTZ(1) = 1
      
      STRIDEX(1) = 1
      STRIDEY(1) = 1
      STRIDEZ(1) = 1
      
      BLOCKX(1) = NXNODE
      BLOCKY(1) = NYNODE
      BLOCKZ(1) = NZNODE
      
      CALL H5SCREATE_SIMPLE_F(NXDIM,GXDIM,SID,ERROR)
      CALL H5SCREATE_SIMPLE_F(NXDIM,LXDIM,MID,ERROR)
      CALL H5PCREATE_F(H5P_DATASET_CREATE_F, PID, ERROR)
      CALL H5PSET_CHUNK_F(PID, NXDIM, LXDIM, ERROR)      
      CALL H5DCREATE_F(GID,'X',H5T_NATIVE_DOUBLE,SID,DID,ERROR)      
      CALL H5SSELECT_HYPERSLAB_F (SID, H5S_SELECT_SET_F,
     $     OFFSETX, COUNTX, ERROR, STRIDEX, BLOCKX)
      CALL H5PCREATE_F(H5P_DATASET_XFER_F, PID, ERROR)
      CALL H5PSET_DXPL_MPIO_F(PID, H5FD_MPIO_COLLECTIVE_F, ERROR)      
      CALL H5DWRITE_F(DID, H5T_NATIVE_DOUBLE, XARRY, GXDIM, ERROR,
     $     FILE_SPACE_ID= SID, MEM_SPACE_ID=MID, XFER_PRP=PID)
      CALL H5DCLOSE_F(DID, ERROR)
      CALL H5PCLOSE_F(PID, ERROR)
      CALL H5SCLOSE_F(MID, ERROR)            
      CALL H5SCLOSE_F(SID, ERROR)

      CALL H5SCREATE_SIMPLE_F(NYDIM,GYDIM,SID,ERROR)
      CALL H5SCREATE_SIMPLE_F(NYDIM,LYDIM,MID,ERROR)
      CALL H5PCREATE_F(H5P_DATASET_CREATE_F, PID, ERROR)
      CALL H5PSET_CHUNK_F(PID, NYDIM, LYDIM, ERROR)      
      CALL H5DCREATE_F(GID,'Y',H5T_NATIVE_DOUBLE,SID,DID,ERROR)      
      CALL H5SSELECT_HYPERSLAB_F (SID, H5S_SELECT_SET_F,
     $     OFFSETY, COUNTY, ERROR, STRIDEY, BLOCKY)
      CALL H5PCREATE_F(H5P_DATASET_XFER_F, PID, ERROR)
      CALL H5PSET_DXPL_MPIO_F(PID, H5FD_MPIO_COLLECTIVE_F, ERROR)      
      CALL H5DWRITE_F(DID, H5T_NATIVE_DOUBLE, YARRY, GYDIM, ERROR,
     $     FILE_SPACE_ID= SID, MEM_SPACE_ID=MID, XFER_PRP=PID)
      CALL H5DCLOSE_F(DID, ERROR)
      CALL H5PCLOSE_F(PID, ERROR)
      CALL H5SCLOSE_F(MID, ERROR)     
      CALL H5SCLOSE_F(SID, ERROR)
      
      CALL H5SCREATE_SIMPLE_F(NZDIM,GZDIM,SID,ERROR)
      CALL H5SCREATE_SIMPLE_F(NZDIM,LZDIM,MID,ERROR)      
      CALL H5PCREATE_F(H5P_DATASET_CREATE_F, PID, ERROR)
      CALL H5PSET_CHUNK_F(PID, NZDIM, LZDIM, ERROR)
      CALL H5DCREATE_F(GID,'Z',H5T_NATIVE_DOUBLE,SID,DID,ERROR)            
      CALL H5SSELECT_HYPERSLAB_F (SID, H5S_SELECT_SET_F,
     $     OFFSETZ, COUNTZ, ERROR, STRIDEZ, BLOCKZ)
      CALL H5PCREATE_F(H5P_DATASET_XFER_F, PID, ERROR)
      CALL H5PSET_DXPL_MPIO_F(PID, H5FD_MPIO_COLLECTIVE_F, ERROR)      
      CALL H5DWRITE_F(DID, H5T_NATIVE_DOUBLE, ZARRY, GZDIM, ERROR,
     $     FILE_SPACE_ID= SID, MEM_SPACE_ID=MID, XFER_PRP=PID)
      CALL H5DCLOSE_F(DID, ERROR)
      CALL H5PCLOSE_F(PID, ERROR)
      CALL H5SCLOSE_F(MID, ERROR)     
      CALL H5SCLOSE_F(SID, ERROR)
      
      CALL H5GCLOSE_F(GID,ERROR)      
      
      CALL H5GCREATE_F(FID,'/Fields',GID,ERROR)      

      OFFSET(1) = IGOFST
      OFFSET(2) = JGOFST
      OFFSET(3) = KGOFST

      COUNT(1) = 1
      COUNT(2) = 1
      COUNT(3) = 1

      STRIDE(1) = 1
      STRIDE(2) = 1
      STRIDE(3) = 1
      
      BLOCK(1) = NXNODE
      BLOCK(2) = NYNODE
      BLOCK(3) = NZNODE

      CALL H5SCREATE_SIMPLE_F(NDIM,GDIM,SID,ERROR)
      CALL H5SCREATE_SIMPLE_F(NDIM,LDIM,MID,ERROR)            
      CALL H5PCREATE_F(H5P_DATASET_CREATE_F, PID, ERROR)
      CALL H5PSET_CHUNK_F(PID, NDIM, LDIM, ERROR)
      CALL H5DCREATE_F(GID,'D',H5T_NATIVE_DOUBLE,SID,DID,ERROR)      
      CALL H5SSELECT_HYPERSLAB_F (SID, H5S_SELECT_SET_F,
     $     OFFSET, COUNT, ERROR, STRIDE, BLOCK)
      CALL H5PCREATE_F(H5P_DATASET_XFER_F, PID, ERROR)
      CALL H5PSET_DXPL_MPIO_F(PID, H5FD_MPIO_COLLECTIVE_F, ERROR)      
      CALL H5DWRITE_F(DID, H5T_NATIVE_DOUBLE, DRUN, GDIM, ERROR,
     $     FILE_SPACE_ID= SID, MEM_SPACE_ID=MID, XFER_PRP=PID)

      CALL H5DCLOSE_F(DID, ERROR)
      CALL H5PCLOSE_F(PID, ERROR)
      CALL H5SCLOSE_F(MID, ERROR)     
      CALL H5SCLOSE_F(SID, ERROR)

C     ENERGY
C     ------
      CALL H5SCREATE_SIMPLE_F(NDIM,GDIM,SID,ERROR)
      CALL H5SCREATE_SIMPLE_F(NDIM,LDIM,MID,ERROR)            
      CALL H5PCREATE_F(H5P_DATASET_CREATE_F, PID, ERROR)
      CALL H5PSET_CHUNK_F(PID, NDIM, LDIM, ERROR)
      CALL H5DCREATE_F(GID,'E',H5T_NATIVE_DOUBLE,SID,DID,ERROR)      
      CALL H5SSELECT_HYPERSLAB_F (SID, H5S_SELECT_SET_F,
     $     OFFSET, COUNT, ERROR, STRIDE, BLOCK)
      CALL H5PCREATE_F(H5P_DATASET_XFER_F, PID, ERROR)
      CALL H5PSET_DXPL_MPIO_F(PID, H5FD_MPIO_COLLECTIVE_F, ERROR)      
      CALL H5DWRITE_F(DID, H5T_NATIVE_DOUBLE, ERUN, GDIM, ERROR,
     $     FILE_SPACE_ID= SID, MEM_SPACE_ID=MID, XFER_PRP=PID)
      
      CALL H5DCLOSE_F(DID, ERROR)
      CALL H5PCLOSE_F(PID, ERROR)
      CALL H5SCLOSE_F(MID, ERROR)     
      CALL H5SCLOSE_F(SID, ERROR)     
      
      CALL H5GCLOSE_F(GID,ERROR)       

      IF(NSPEC.GT.1)THEN
         
         CALL H5GCREATE_F(FID,'/Fields/Composition',GID,ERROR)      

         DO ISPEC=1,NSPM1

            WRITE(SPECVN,'(A1,I2.2)') 'Y', ISPEC

            CALL H5SCREATE_SIMPLE_F(NDIM,GDIM,SID,ERROR)
            CALL H5SCREATE_SIMPLE_F(NDIM,LDIM,MID,ERROR)            
            CALL H5PCREATE_F(H5P_DATASET_CREATE_F, PID, ERROR)
            CALL H5PSET_CHUNK_F(PID, NDIM, LDIM, ERROR)
            CALL H5DCREATE_F(GID,SPECVN,H5T_NATIVE_DOUBLE,SID,DID,ERROR)      
            CALL H5SSELECT_HYPERSLAB_F (SID, H5S_SELECT_SET_F,
     $           OFFSET, COUNT, ERROR, STRIDE, BLOCK)
            CALL H5PCREATE_F(H5P_DATASET_XFER_F, PID, ERROR)
            CALL H5PSET_DXPL_MPIO_F(PID, H5FD_MPIO_COLLECTIVE_F, ERROR)      
            CALL H5DWRITE_F(DID, H5T_NATIVE_DOUBLE,
     $           YRUN(:,:,:,ISPEC), GDIM, ERROR,
     $           FILE_SPACE_ID= SID, MEM_SPACE_ID=MID, XFER_PRP=PID)
            CALL H5DCLOSE_F(DID, ERROR)
            CALL H5PCLOSE_F(PID, ERROR)
            CALL H5SCLOSE_F(MID, ERROR)     
            CALL H5SCLOSE_F(SID, ERROR)
            
         ENDDO
         
         CALL H5GCLOSE_F(GID,ERROR)          

      ENDIF
         
      CALL H5GCREATE_F(FID,'/Fields/Velocity',GID,ERROR)

      CALL H5SCREATE_SIMPLE_F(NDIM,GDIM,SID,ERROR)
      CALL H5SCREATE_SIMPLE_F(NDIM,LDIM,MID,ERROR)            
      CALL H5PCREATE_F(H5P_DATASET_CREATE_F, PID, ERROR)
      CALL H5PSET_CHUNK_F(PID, NDIM, LDIM, ERROR)
      CALL H5DCREATE_F(GID,'U',H5T_NATIVE_DOUBLE,SID,DID,ERROR)      
      CALL H5SSELECT_HYPERSLAB_F (SID, H5S_SELECT_SET_F,
     $     OFFSET, COUNT, ERROR, STRIDE, BLOCK)
      CALL H5PCREATE_F(H5P_DATASET_XFER_F, PID, ERROR)
      CALL H5PSET_DXPL_MPIO_F(PID, H5FD_MPIO_COLLECTIVE_F, ERROR)      
      CALL H5DWRITE_F(DID, H5T_NATIVE_DOUBLE, URUN, GDIM, ERROR,
     $     FILE_SPACE_ID= SID, MEM_SPACE_ID=MID, XFER_PRP=PID)
      CALL H5DCLOSE_F(DID, ERROR)
      CALL H5PCLOSE_F(PID, ERROR)
      CALL H5SCLOSE_F(MID, ERROR)     
      CALL H5SCLOSE_F(SID, ERROR)
      
      CALL H5SCREATE_SIMPLE_F(NDIM,GDIM,SID,ERROR)
      CALL H5SCREATE_SIMPLE_F(NDIM,LDIM,MID,ERROR)            
      CALL H5PCREATE_F(H5P_DATASET_CREATE_F, PID, ERROR)
      CALL H5PSET_CHUNK_F(PID, NDIM, LDIM, ERROR)
      CALL H5DCREATE_F(GID,'V',H5T_NATIVE_DOUBLE,SID,DID,ERROR)      
      CALL H5SSELECT_HYPERSLAB_F (SID, H5S_SELECT_SET_F,
     $     OFFSET, COUNT, ERROR, STRIDE, BLOCK)
      CALL H5PCREATE_F(H5P_DATASET_XFER_F, PID, ERROR)
      CALL H5PSET_DXPL_MPIO_F(PID, H5FD_MPIO_COLLECTIVE_F, ERROR)      
      CALL H5DWRITE_F(DID, H5T_NATIVE_DOUBLE, VRUN, GDIM, ERROR,
     $     FILE_SPACE_ID= SID, MEM_SPACE_ID=MID, XFER_PRP=PID)
      CALL H5DCLOSE_F(DID, ERROR)
      CALL H5PCLOSE_F(PID, ERROR)
      CALL H5SCLOSE_F(MID, ERROR)     
      CALL H5SCLOSE_F(SID, ERROR)

      CALL H5SCREATE_SIMPLE_F(NDIM,GDIM,SID,ERROR)
      CALL H5SCREATE_SIMPLE_F(NDIM,LDIM,MID,ERROR)            
      CALL H5PCREATE_F(H5P_DATASET_CREATE_F, PID, ERROR)
      CALL H5PSET_CHUNK_F(PID, NDIM, LDIM, ERROR)
      CALL H5DCREATE_F(GID,'W',H5T_NATIVE_DOUBLE,SID,DID,ERROR)      
      CALL H5SSELECT_HYPERSLAB_F (SID, H5S_SELECT_SET_F,
     $     OFFSET, COUNT, ERROR, STRIDE, BLOCK)
      CALL H5PCREATE_F(H5P_DATASET_XFER_F, PID, ERROR)
      CALL H5PSET_DXPL_MPIO_F(PID, H5FD_MPIO_COLLECTIVE_F, ERROR)      
      CALL H5DWRITE_F(DID, H5T_NATIVE_DOUBLE, WRUN, GDIM, ERROR,
     $     FILE_SPACE_ID= SID, MEM_SPACE_ID=MID, XFER_PRP=PID)
      CALL H5DCLOSE_F(DID, ERROR)
      CALL H5PCLOSE_F(PID, ERROR)
      CALL H5SCLOSE_F(MID, ERROR)     
      CALL H5SCLOSE_F(SID, ERROR)
      
      CALL H5GCLOSE_F(GID,ERROR)         
         
C     TERMINATE ACCESS TO MEMORY AND FILE SPACE
      CALL H5FCLOSE_F(FID, ERROR)
      CALL H5CLOSE_F(ERROR)

      RETURN
      END
      
